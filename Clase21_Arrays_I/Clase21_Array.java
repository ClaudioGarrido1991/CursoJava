package Clase21_Arrays_I;

/*Matrices (Arrays o Arreglos).
 * 
 * ¿Que es? Es una estructura que contiene muchos valores del mismo tipo.
 * 
 * ¿Para que? Para almacenar valores que estan relacionados.
 * 
 * Sintaxis:
 *  int [] mi_matriz = new int [10];
 * tipo-->nombre-->numero_elementos;
 * 
 * También las matrices pueden ser declaradas y luego inicializadas mas tarde.
 * Las posiciones de las matrices inician en el cero.
 * 
 */

public class Clase21_Array {
    public static void main(String[] args) {

        /*Valore iniciales */

        int [] mi_matriz = new int [5]; //int es con minúscula, si no da error.

        mi_matriz [0] = 1;
        mi_matriz [1] = 2;
        mi_matriz [2] = 3;
        mi_matriz [3] = 4;
        mi_matriz [4] = 5;

        /*Declaración simplificada alternativa */

        int [] mi_matriz_2 = {1,2,3,4,5};

        


        

    }


    
}
